def get_day_name(day_number):
    if day_number == 1:
        return "Lundi"
    elif day_number == 2:
        return "Mardi"
    elif day_number == 3:
        return "Mercredi"
    elif day_number == 4:
        return "Jeudi"
    elif day_number == 5:
        return "Vendredi"
    elif day_number == 6:
        return "Samedi"
    elif day_number == 7:
        return "Dimanche"
    return ""


